package com.example.demo.mode.myenum;

public enum BaseRetCode {

    SUCCESS(10000,"成功!"),

    FAIL(9999,"失败!");

    private BaseRetCode(int code, String name) {
        this.code = code;
        this.name = name;
    }

    private int code;

    private String name;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
